using System.Net;
using System.Web;
using System.Web.Mvc;
using najdiPrevoz.Models;

namespace najdiPrevoz.Controllers
{
    public class KorisniksController : Controller
    {
        private ApplicationDbContext db = new ApplicationDbContext();
        
